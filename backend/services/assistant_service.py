def assistant_response(user_query: str, language: str = "en") -> str:
    """Returns answer in the selected language."""
    lang = (language or "en").lower().strip()
    if lang not in TRANSLATIONS:
        lang = "en"
    intent = _detect_intent(user_query)
    if intent is not None:
        return TRANSLATIONS[lang][intent]
    return _get_fallback_message(lang)


def get_supported_languages():
    return [{"code": code, "name": name} for code, name in LANGUAGES.items()]


